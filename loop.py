# WHILE LOOP

# -------------INCREMENT---------------
count = 0
while count <= 10:
    print(count)
    count += 1
    


# -------------DECREMENT---------------
count1 = 10
while count1 >= 0:
    print(count1)
    count1 -= 1
    
    

# -------------USING RANGE---------------
i = 0
while i in range(5):
    print(i)
    i+=1